#include <stdio.h>

#ifdef NULL
#undef NULL
#endif

#include <std/print.h>
#include <std/strings.h>


int
k_print(const char *format, ...)
{
	int ret;
	va_list args;

	va_start(args, format);
	ret = vfprintf(stdout, format, args);
	va_end(args);

	return ret;
}

